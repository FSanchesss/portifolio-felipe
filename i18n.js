/* ── Apply translations ─────────────────────── */
function applyLang(lang) {
    const t = window.TRANSLATIONS[lang];
    if (!t) return;

    localStorage.setItem('portfolioLang', lang);
    document.documentElement.lang = lang;

    /* 1. Single-element selector map */
    const singleMap = [
        ['.hero-badge',                         'hero.badge'],
        ['.hero-heading',                       'hero.heading'],
        ['.hero-desc',                          'hero.desc'],
        ['.clients-label',                      'clients.label'],
        ['.works-tag-pill',                     'works.tag'],
        ['.works-title',                        'works.title'],
        ['.about-card-title',                   'about.title'],
        ['.about-card-text p:nth-child(1)',     'about.p1'],
        ['.about-card-text p:nth-child(2)',     'about.p2'],
        ['.about-card-text p:nth-child(3)',     'about.p3'],
        ['.about-stat:nth-child(1) span',       'about.stat1'],
        ['.about-stat:nth-child(2) span',       'about.stat2'],
        ['.about-stat:nth-child(3) span',       'about.stat3'],
        ['.about-actions a:nth-child(1)',       'about.btn1'],
        ['.about-actions a:nth-child(2)',       'about.btn2'],
        ['.tools-title',                        'tools.title'],
        ['.contact-tag',                        'contact.tag'],
        ['.contact-title',                      'contact.title'],
        ['.contact-desc',                       'contact.desc'],
        ['.footer-big',                         'footer.big'],
        ['.footer-social-label',               'footer.social'],
        ['.footer-nav a:nth-child(1)',          'footer.works'],
        ['.footer-nav a:nth-child(2)',          'footer.about'],
        ['.footer-nav a:nth-child(3)',          'footer.faq'],
        ['.footer-nav a:nth-child(4)',          'footer.services'],
        ['.footer-nav a:nth-child(5)',          'footer.articles'],
        ['.service-block:first-child .service-tag',   'services1.tag'],
        ['.service-block:first-child .service-title', 'services1.title'],
        ['.service-block:first-child .service-desc',  'services1.desc'],
        ['.service-block:last-child .service-tag',    'services2.tag'],
        ['.service-block:last-child .service-title',  'services2.title'],
        ['.service-block:last-child .service-desc',   'services2.desc'],
    ];
    singleMap.forEach(([sel, key]) => {
        const el = document.querySelector(sel);
        if (el && t[key] !== undefined) el.innerHTML = t[key];
    });

    /* 2. Multi-instance elements */
    const multiMap = [
        ['.btn-primary',    'hero.cta1'],
        ['.btn-cta',        'nav.cta'],
        ['.btn-courses',    'nav.courses'],
        ['.work-view-btn',  'works.viewBtn'],
        ['.service-block .btn-primary', 'services.cta'],
    ];
    multiMap.forEach(([sel, key]) => {
        if (t[key] === undefined) return;
        document.querySelectorAll(sel).forEach(el => el.innerHTML = t[key]);
    });

    /* 3. Nav links */
    const navKeys = ['nav.works', 'nav.about', 'nav.services', 'nav.contact'];
    document.querySelectorAll('.nav-links a.nav-link').forEach((el, i) => {
        if (t[navKeys[i]]) el.textContent = t[navKeys[i]];
    });
    document.querySelectorAll('.mobile-menu ul li a').forEach((el, i) => {
        if (t[navKeys[i]]) el.textContent = t[navKeys[i]];
    });

    /* 4. Work card descriptions + data-desc for project page */
    document.querySelectorAll('.work-card[data-i18n-desc]').forEach(card => {
        const key = card.dataset.i18nDesc;
        if (t[key] !== undefined) {
            card.dataset.desc = t[key];
            const p = card.querySelector('.work-card-desc');
            if (p) p.textContent = t[key];
        }
    });

    /* 5. Inline data-i18n spans (contact btns, explore btn) */
    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.dataset.i18n;
        if (t[key] !== undefined) el.textContent = t[key];
    });

    /* 6. Filter "All" button */
    const allBtn = document.querySelector('.filter-btn[data-filter="all"]');
    if (allBtn && t['works.filter.all']) allBtn.textContent = t['works.filter.all'];

    /* 7. Lang label update */
    document.querySelectorAll('.lang-label').forEach(el => {
        el.textContent = lang.toUpperCase();
    });
}

function toggleLang() {
    const current = localStorage.getItem('portfolioLang') || 'en';
    applyLang(current === 'en' ? 'pt' : 'en');
}

/* ── Auto-detect on load ────────────────────── */
(function initI18n() {
    const saved      = localStorage.getItem('portfolioLang');
    const browserLang = (navigator.language || 'en').toLowerCase();
    const auto       = browserLang.startsWith('pt') ? 'pt' : 'en';
    applyLang(saved || auto);
})();
